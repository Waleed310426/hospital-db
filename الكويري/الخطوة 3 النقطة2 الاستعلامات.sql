USE HospitalDB1; -- تأكد من استخدام قاعدة البيانات الصحيحة
GO

-- =================================================================
-- الجزء الأول: استعلامات الاسترجاع البسيطة مع الشروط والترتيب
-- =================================================================
PRINT N'--- [1] استعلام بسيط: عرض قائمة بأسماء المرضى وأرقام هواتفهم ---';

-- SELECT: لتحديد الأعمدة التي نريد عرضها
SELECT 
    FullName,       -- عرض حقل الاسم الكامل
    PhoneNumber,    -- عرض حقل رقم الهاتف
    Address         -- عرض حقل العنوان
-- FROM: لتحديد الجدول الذي سنأخذ منه البيانات
FROM 
    Patients
-- ORDER BY: لترتيب النتائج
ORDER BY 
    FullName ASC;   -- ASC تعني ترتيب أبجدي تصاعدي (من الألف إلى الياء)
GO

-- -----------------------------------------------------------------
PRINT N'--- [2] استعلام بشرط واحد: عرض الأطباء الذين تخصصهم "طب الأطفال" فقط ---';

-- SELECT: نختار اسم الطبيب الكامل واسم التخصص
SELECT
    Doctors.FullName,      -- اسم الطبيب من جدول الأطباء
    Specialties.SpecialtyName -- اسم التخصص من جدول التخصصات
-- FROM: نبدأ بجدول الأطباء
FROM
    Doctors
-- JOIN: نربط جدول الأطباء بجدول التخصصات
-- ON: نحدد شرط الربط، وهو أن يتساوى مفتاح التخصص في كلا الجدولين
JOIN
    Specialties ON Doctors.SpecialtyID = Specialties.SpecialtyID
-- WHERE: لتصفية (فلترة) النتائج وإظهار ما نريده فقط
WHERE
    Specialties.SpecialtyName = N'أطفال'; -- الشرط هو أن يكون اسم التخصص "طب الأطفال"
GO

-- -----------------------------------------------------------------
PRINT N'--- [3] استعلام بشرطين: عرض الفواتير "غير المدفوعة" والتي تزيد قيمتها عن 10000 ريال ---';

SELECT
    p.FullName,         -- اسم المريض (استخدمنا p كاسم مختصر لجدول Patients)
    i.InvoiceID,        -- رقم الفاتورة (استخدمنا i كاسم مختصر لجدول Invoices)
    i.TotalAmount,      -- المبلغ الإجمالي للفاتورة
    i.InvoiceDate       -- تاريخ الفاتورة
FROM
    Invoices AS i       -- نحدد جدول الفواتير ونعطيه اسماً مختصراً هو i
JOIN
    Patients AS p ON i.PatientID = p.PatientID -- نربطه بجدول المرضى الذي نعطيه اسماً مختصراً هو p
JOIN
    InvoiceStatus AS ist ON i.InvoiceStatusID = ist.InvoiceStatusID -- نربطه بجدول حالات الفواتير
WHERE
    ist.StatusName = N'غير مدفوعة'  -- الشرط الأول: أن تكون حالة الفاتورة "غير مدفوعة"
    AND i.TotalAmount > 10000      -- AND تعني "و"، أي يجب تحقق الشرط الثاني أيضاً
ORDER BY
    i.TotalAmount DESC; -- DESC تعني ترتيب تنازلي (من الأعلى إلى الأقل)
GO

-- =================================================================
-- الجزء الثاني: استعلامات التجميع (تلخيص البيانات)
-- =================================================================

-- -----------------------------------------------------------------
PRINT N'--- [5] استعلام تجميعي: حساب إجمالي الإيرادات من كل نوع غرفة ---';

SELECT
    rt.RoomTypeName, -- اسم نوع الغرفة
    -- SUM(): دالة تقوم بجمع القيم
    SUM(rt.CostPerDay) AS TotalRevenue, -- اسم مستعار بدون مسافات
    -- COUNT(): دالة تقوم بعدّ عدد الحجوزات لكل نوع غرفة
    COUNT(ba.AssignmentID) AS NumberOfBookings -- اسم مستعار بدون مسافات
FROM
    BedAssignments AS ba -- جدول حجوزات الأسرة
JOIN
    Rooms AS r ON ba.RoomID = r.RoomID -- نربطه بجدول الغرف
JOIN
    RoomTypes AS rt ON r.RoomTypeID = rt.RoomTypeID -- نربطه بجدول أنواع الغرف
GROUP BY
    rt.RoomTypeName -- نجمع النتائج حسب نوع الغرفة
ORDER BY
    TotalRevenue DESC; -- **تم التصحيح: الآن نرتب باستخدام الاسم المستعار الصحيح**
GO

-- -----------------------------------------------------------------
PRINT N'--- [6] استعلام تجميعي: عرض ملخص مالي بسيط (إجمالي الإيرادات، متوسط الفواتير) ---';

SELECT
    -- SUM(TotalAmount): حساب مجموع كل المبالغ في عمود TotalAmount
    SUM(TotalAmount) AS 'إجمالي إيرادات المستشفى',
    -- AVG(TotalAmount): حساب متوسط قيمة الفاتورة الواحدة
    AVG(TotalAmount) AS 'متوسط قيمة الفاتورة',
    -- COUNT(InvoiceID): حساب العدد الكلي للفواتير الصادرة
    COUNT(InvoiceID) AS 'العدد الكلي للفواتير'
FROM
    Invoices; -- من جدول الفواتير
GO

-- =================================================================
-- الجزء الثالث: استعلامات تستخدم دوال أخرى مفيدة
-- =================================================================
PRINT N'--- [7] استعلام يستخدم دوال التاريخ: عرض المواعيد المجدولة للأسبوع القادم ---';

SELECT
    p.FullName AS 'اسم المريض',
    d.FullName AS 'اسم الطبيب',
    a.AppointmentDateTime AS 'تاريخ ووقت الموعد'
FROM
    Appointments a
JOIN
    Patients p ON a.PatientID = p.PatientID
JOIN
    Doctors d ON a.DoctorID = d.DoctorID
WHERE
    -- GETDATE(): دالة ترجع التاريخ والوقت الحالي
    -- DATEADD(day, 7, GETDATE()): دالة تضيف 7 أيام للتاريخ الحالي
    -- الشرط يعني: أن يكون تاريخ الموعد بين اليوم والسبعة أيام القادمة
    a.AppointmentDateTime BETWEEN GETDATE() AND DATEADD(day, 7, GETDATE())
ORDER BY
    a.AppointmentDateTime ASC;
GO

-- -----------------------------------------------------------------
PRINT N'--- [8] استعلام يستخدم LIKE للبحث المتقدم: البحث عن أي مريض اسمه يبدأ بـ "محمد" ---';

SELECT
    FullName,
    PhoneNumber,
    Address
FROM
    Patients
WHERE
    -- LIKE: تستخدم للبحث عن نمط معين في النصوص
    -- N'محمد%': علامة % تعني "أي عدد من الحروف"، فالشرط هو أن يبدأ الاسم بـ "محمد"
    FullName LIKE N'محمد%';
GO

